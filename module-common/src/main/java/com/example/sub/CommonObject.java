package com.example.sub;

public class CommonObject {
    Integer index;
    String value;

    public void setIndex(Integer index) {
        this.index = index;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public Integer getIndex() {
        return index;
    }

    public String getValue() {
        return value;
    }
}
